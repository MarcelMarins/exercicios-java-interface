    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
       
       
       double total = 0;
       double deposito = 0; 
       int num_pag = 0;
       int anos = 0;
       double juros = 0;
       
        juros = Double.parseDouble(jTextField1.getText());
        anos = Integer.parseInt(jTextField2.getText());
        deposito = Double.parseDouble(jTextField3.getText());
        num_pag = anos * 12;
        juros = juros / 100;
        
        for (int i=0; i<num_pag; i++){
            
            total = total + deposito;
            total = total + total * juros;
 
        }
      
        jTextField4.setText(String.valueOf(total));
        
        
    }//GEN-LAST:event_jToggleButton1ActionPerformed

